using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Whileloop_prog
{
    class Program
    {
        static void Main(string[] args)
        {
            //while loop

            int i = 1;
            while (i <= 10)
            {
                Console.WriteLine(i);
                i++;
            }
            Console.ReadLine();
        }
    }
}
