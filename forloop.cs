using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace forloop_program
{
    class Program
    {
        static void Main(string[] args)
        {
            //for loop
            for (int i = 1; i <= 10; i++)
            {
                Console.WriteLine(i);
            }
            Console.ReadLine();

        }
    }
}
